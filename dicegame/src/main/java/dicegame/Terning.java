package dicegame;

public class Terning {

    int id;
    int verdi;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the verdi
     */
    public int getVerdi() {
        return verdi;
    }

    /**
     * @param verdi the verdi to set
     */
    public void setVerdi(int verdi) {
        this.verdi = verdi;
    }

}